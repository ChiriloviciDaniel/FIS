    private void updateTable() {

        try {
            String sql = "SELECT * FROM comenzi";
            ps = conn.prepareStatement(sql);
            rs = ps.executeQuery();
            tableComenzi.setModel(DbUtils.resultSetToTableModel(rs));
            rs.close();
            ps.close();
            // conn.close();

        } catch (SQLException b) {
            JOptionPane.showMessageDialog(null, b);
        }
    }

